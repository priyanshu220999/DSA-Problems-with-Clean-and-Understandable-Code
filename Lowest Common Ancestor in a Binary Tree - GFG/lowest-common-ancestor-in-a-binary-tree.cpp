class Solution
{
    bool rootPath(Node* root,int target,vector<Node*> &path){
        
        if(!root)
            return false;
            
        path.push_back(root);
        
        if(root->data==target){
            return true;
        }
        
        if(rootPath(root->left,target,path) || rootPath(root->right,target,path)){
            return true;
        }
        
        path.pop_back();
        
        return false;
    }
    
    public:
    //Function to return the lowest common ancestor in a Binary Tree.
    Node* lca(Node* root ,int n1 ,int n2 )
    {
       //Your code here 
       
       vector<Node*> n1Path;
       vector<Node*> n2Path;
       
       rootPath(root,n1,n1Path);
       rootPath(root,n2,n2Path);
       
       Node* commonAncestor = root;
       
       int n = n1Path.size();
       int m = n2Path.size();
       int size = n<m?n:m;
       int i = 1;
       while(i<size){
           
           if(n1Path[i]==n2Path[i]){
               commonAncestor = n1Path[i];
           }
           else{
               break;
           }
           i++;
       }
       return commonAncestor;
    }
};

